void Code_KevesParse::write(KevcGenerator* generator) {
  if (!generator->setImportLibrary("keves", "base-bin", 0, 1)) return;
  // for invoking makeAssertCondition()
  generator->importBind("&assert");
  generator->importBind("&message");
  generator->importBind("&irritants");
  generator->importBind("&who");
 
  // if (!generator->setImportLibrary("keves", "base", 0, 1)) return;
  KevesValue proc_u8_list_to_vector(generator->importBind("u8-list->vector"));
 
  if (!generator->setImportLibrary("rnrs", "base", 6)) return;
  KevesValue proc_add(generator->importBind("+"));
  KevesValue proc_equal(generator->importBind("="));
  KevesValue proc_less_than(generator->importBind("<"));
  KevesValue proc_less_than_equal(generator->importBind("<="));
  KevesValue proc_greater_than(generator->importBind(">"));
  KevesValue proc_greater_than_equal(generator->importBind(">="));
  KevesValue proc_append(generator->importBind("append"));
  KevesValue proc_apply(generator->importBind("apply"));
  KevesValue proc_assertion_violation(generator->importBind("assertion-violation"));
  KevesValue proc_call_with_values(generator->importBind("call-with-values"));
  KevesValue proc_car(generator->importBind("car"));
  KevesValue proc_caar(generator->importBind("caar"));
  KevesValue proc_caadr(generator->importBind("caadr"));
  KevesValue proc_cdr(generator->importBind("cdr"));
  KevesValue proc_cdar(generator->importBind("cdar"));
  KevesValue proc_cdadr(generator->importBind("cdadr"));
  KevesValue proc_cddr(generator->importBind("cddr"));
  KevesValue proc_char_eq_q(generator->importBind("char=?"));
  KevesValue proc_char_lt_q(generator->importBind("char<?"));
  KevesValue proc_char_gt_q(generator->importBind("char>?"));
  KevesValue proc_cons(generator->importBind("cons"));
  KevesValue proc_integer_to_char(generator->importBind("integer->char"));
  KevesValue proc_list(generator->importBind("list"));
  KevesValue proc_list_to_vector(generator->importBind("list->vector"));
  KevesValue proc_not(generator->importBind("not"));
  KevesValue proc_null_q(generator->importBind("null?"));
  KevesValue proc_pair_q(generator->importBind("pair?"));
  KevesValue proc_reverse(generator->importBind("reverse"));
  KevesValue proc_string(generator->importBind("string"));
  KevesValue proc_string_eq_q(generator->importBind("string=?"));
  KevesValue proc_string_append(generator->importBind("string-append"));
  KevesValue proc_string_length(generator->importBind("string-length"));
  KevesValue proc_string_ref(generator->importBind("string-ref"));
  KevesValue proc_string_to_list(generator->importBind("string->list"));
  KevesValue proc_string_to_number(generator->importBind("string->number"));
  KevesValue proc_string_to_symbol(generator->importBind("string->symbol"));
  KevesValue proc_substring(generator->importBind("substring"));
  KevesValue proc_symbol_eq_q(generator->importBind("symbol=?"));
  KevesValue proc_values(generator->importBind("values"));
  KevesValue proc_zero_q(generator->importBind("zero?"));

  if (!generator->setImportLibrary("rnrs", "lists", 6)) return;
  KevesValue proc_fold_left(generator->importBind("fold-left"));

  if (!generator->setImportLibrary("rnrs", "unicode", 6)) return;
  KevesValue proc_char_general_category(generator->importBind("char-general-category"));
  KevesValue proc_char_whitespace_q(generator->importBind("char-whitespace?"));
  KevesValue proc_char_ci_lt_q(generator->importBind("char-ci<?"));
  KevesValue proc_char_ci_gt_q(generator->importBind("char-ci>?"));

  // export binds
  generator->exportBind("keves-parser",
			makeLambda_qeves_parser(generator,
						proc_u8_list_to_vector,
						proc_add,
						proc_equal,
						proc_less_than,
						proc_less_than_equal,
						proc_greater_than,
						proc_greater_than_equal,
						proc_append,
						proc_apply,
						proc_assertion_violation,
						proc_call_with_values,
						proc_car,
						proc_caar,
						proc_caadr,
						proc_cdr,
						proc_cdar,
						proc_cdadr,
						proc_cddr,
						proc_char_eq_q,
						proc_char_lt_q,
						proc_char_gt_q,
						proc_cons,
						proc_integer_to_char,
						proc_list,
						proc_list_to_vector,
						proc_not,
						proc_null_q,
						proc_pair_q,
						proc_reverse,
						proc_string,
						proc_string_eq_q,
						proc_string_append,
						proc_string_length,
						proc_string_ref,
						proc_string_to_list,
						proc_string_to_number,
						proc_string_to_symbol,
						proc_substring,
						proc_symbol_eq_q,
						proc_values,
						proc_zero_q,
						proc_fold_left,
						proc_char_general_category,
						proc_char_whitespace_q,
						proc_char_ci_lt_q,
						proc_char_ci_gt_q));
  
  generator->writeToFile();
}

